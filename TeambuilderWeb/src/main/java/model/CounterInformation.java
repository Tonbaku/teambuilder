package model;

public class CounterInformation {
	private double winRatio;
	private double koRatio;
	private double switchRatio;
	public double getWinRatio() {
		return winRatio;
	}
	public void setWinRatio(double winRatio) {
		this.winRatio = winRatio;
	}
	public double getKoRatio() {
		return koRatio;
	}
	public void setKoRatio(double koRatio) {
		this.koRatio = koRatio;
	}
	public double getSwitchRatio() {
		return switchRatio;
	}
	public void setSwitchRatio(double switchRatio) {
		this.switchRatio = switchRatio;
	}
}
